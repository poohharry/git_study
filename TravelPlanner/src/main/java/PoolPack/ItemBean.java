package PoolPack;

public class ItemBean {
	String name;	// 관광지 이름
	String addr;	// 이미지 주소
	String intro;	// 소개글


	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getAddr() {
		return addr;
	}
	
	public void setAddr(String addr) {
		this.addr = addr;
	}

	public String getIntro() {
		return intro;
	}

	public void setIntro(String intro) {
		this.intro = intro;
	}
}
